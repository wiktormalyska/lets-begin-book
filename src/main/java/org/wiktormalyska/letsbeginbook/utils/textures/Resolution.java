package org.wiktormalyska.letsbeginbook.utils.textures;

public class Resolution {
    private int x;
    private int y;

    public int getX() {
        return x;
    }
    public Resolution(int x, int y) {
        this.x = x;
        this.y = y;
    }


    public int getY() {
        return y;
    }

}
